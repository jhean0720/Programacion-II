    private void registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarActionPerformed
         String Nombre = nombre.getText();
         String NumeroIdentidad = cedula.getText();
         String Telefono = telefono.getText();
         String Direccion = direccion.getText();
         String Localidad = localidad.getText();
         
        try{
            Connection conexion = Conexion.getConexion();
            System.out.println(conexion);
         PreparedStatement ps = conexion.prepareStatement("INSERT INTO practica(Nombre, NumeroIdentidad, Telefono, Direccion, Localidad) VALUES (?, ?, ?, ?, ?, ?)");
         ps.setString(1, Nombre);
         ps.setString(2, NumeroIdentidad);
         ps.setString(3, Telefono); 
         ps.setString(4, Direccion);
         ps.setString(5, Localidad);
         ps.execute();
     }catch(Exception e){
         System.out.println(e);
     }
    }//GEN-LAST:event_registrarActionPerformed

